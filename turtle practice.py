# Uses the turtle module to create basic shapes

#Imports necessary turtle commands
import turtle

#The following lines make a square
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)

#This command keeps the window from closing immediately
turtle.exitonclick()